def run_edgeflip_from_saved(
    sim_dir: str | Path,
    rep_ids: Sequence[int],
    K: int,
    eps_list: Iterable[float],
    delta: float = 0.0,
    nstart: int = 25,
    base_seed: int = 5000,
    verbose: bool = True,
    index_base_edges: Optional[int] = 1,
    max_added_non_edges: Optional[int] = None,
) -> pd.DataFrame:
    """
    Run the EdgeFlip baseline on saved simulation replications.

    Parameters
    ----------
    sim_dir:
        Scenario folder containing replication subfolders.
    rep_ids:
        Replication IDs to use.
    K:
        Number of communities.
    eps_list:
        Privacy budgets for EdgeFlip.  In Experiment 2 these are the total
        budgets ``eps_all``.
    delta:
        Approximate-DP slack used in the keep/flip probabilities.  Set to 0 for
        the standard pure-DP randomized response version.
    nstart:
        Number of k-means initializations.
    base_seed:
        Base random seed.  The actual seed depends on both replication and
        privacy budget index.
    verbose:
        If True, print one line per run.
    index_base_edges:
        Index base for CSV edge lists.  Bit-packed files are decoded as 0-based
        internally regardless of this value.
    max_added_non_edges:
        Optional safety cap on the number of non-edges added by EdgeFlip.
    """
    sim_dir = Path(sim_dir)
    eps_values = [float(eps) for eps in eps_list]

    rows: list[dict] = []
    for rep in rep_ids:
        rep_dir = _find_rep_dir(sim_dir, int(rep))
        n, ei, ej = load_saved_upper_edges(rep_dir, index_base_edges=index_base_edges)
        z_true = try_load_labels_true(str(rep_dir))
        if z_true is None:
            raise FileNotFoundError(f"Cannot load true labels from {rep_dir}.")
        z_true = np.asarray(z_true, dtype=int).reshape(-1)
        if z_true.size != n:
            raise ValueError(f"Label length mismatch in {rep_dir}: len(z)={z_true.size}, n={n}.")

        for eps_idx, eps in enumerate(eps_values):
            seed = int(base_seed + 1_000_003 * int(rep) + eps_idx)
            rng = np.random.default_rng(seed)

            ei_priv, ej_priv, p_keep, q_flip, n_kept, n_added = edgeflip_upper_edges(
                n=n,
                ei=ei,
                ej=ej,
                eps=eps,
                rng=rng,
                delta=delta,
                max_added_non_edges=max_added_non_edges,
            )
            A_priv = _build_sparse_A_from_upper_edges(n=n, ei=ei_priv, ej=ej_priv)
            Xi_priv = _top_abs_eigvecs(A_priv, K=K)
            labels = _cluster_from_embedding(
                Xi_priv,
                K=K,
                nstart=nstart,
                seed=base_seed + int(rep),
            )
            err = clustering_error(z_true, labels)

            rows.append(
                dict(
                    rep=int(rep),
                    eps=float(eps),
                    delta=float(delta),
                    clustering_error_vs_truth=float(err),
                    n=int(n),
                    num_edges_original=int(ei.size),
                    num_edges_private=int(ei_priv.size),
                    num_edges_kept=int(n_kept),
                    num_non_edges_added=int(n_added),
                    p_keep=float(p_keep),
                    q_flip=float(q_flip),
                    seed=int(seed),
                )
            )

            if verbose:
                print(
                    f"[EdgeFlip] rep={int(rep):03d}, eps={eps:.4g}, "
                    f"err={err:.6f}, kept={n_kept:,}, added={n_added:,}"
                )

    return pd.DataFrame(rows)


