"""
EdgeFlip utility for Experiment 2.

This module implements the edge-flipping baseline for undirected network
community detection.  It is intended to be placed under ``Utility/`` and
imported by experiment scripts, for example

    from Utility.Edge_flip import run_edgeflip_from_saved

The input folders are the saved DCSBM replications generated by the simulation
utility.  Each replication folder should contain either

    A_upper_bitpack.bin

or an edge-list file such as

    A_edges.csv
    edges.csv

and should also contain ``A_eigvecs_topK.csv`` with the true labels ``z`` for
evaluating clustering error.

Main public function
--------------------
``run_edgeflip_from_saved``
    For each saved replication and each privacy budget epsilon, apply the
    edge-flipping mechanism, run spectral clustering on the privatized graph,
    and return a replication-level pandas DataFrame.
"""

